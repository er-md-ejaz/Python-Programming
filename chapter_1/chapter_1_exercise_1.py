# ///// Print these sequences using escape sequence not using normal spaces

# this is double \\ backslash
print("this is double \\\\ backslash")

# these are /\/\/\/\/\ mountains
print("these are /\\/\\/\\/\\/\\")

# he is     awesome
print("he is \t awesome")

# \" \n \t \'
print("\\\" \\n \\t \\\'")

