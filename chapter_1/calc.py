print(2+4*5)

#floating point division
print(2/4)
print(6/2)

# Integer division
print(2//4)
print(6//2)

# Exponent(**)
print(2**4)
print(5**0.5)
print(round(5**0.5, 3))

# To get remainder we need to perform modulo operator
print(5%3)

# Precedence and associativity rule, 
# when precedence rule is violated then associativity rule comes into picture
print(2**3/2*6-4*(3-4/2))
print(2**3**2)   #associativity from right to left and answer would be 512
